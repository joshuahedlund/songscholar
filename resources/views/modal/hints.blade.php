<p><b>What makes a good reference?</b></p>

<p>A good reference is a phrase from a song that clearly quotes a specific phrase from the Bible, either word-for-word to a specific version, or nearly word-for-word, or an obvious paraphrase, so that it seems clear that the songwriter was thinking of that verse when they wrote that line.</p>

<p>If the lyric spans multiple verses but specifically refers to each one, you should add a reference for each verse.</p>

<p>If the lyric does not specifically refer to each verse but makes a more general reference, it is not currently a good fit for this site (although support may be added by demand in the future).</p>

<p>Example. When Arcade Fire sings,</p>

	<blockquote>“Never trust a millionaire quoting the Sermon on the Mount”</blockquote>

<p>This is a reference to Matthew 5-7, but it is too broad and does not quote from any specific verses. This is not a good reference for SongRefs.</p>

<p>However, when Coldplay sings,</p>

	<p>“Saying Lord lead me not into temptation”</p>

<p>This is clearly a reference to the Lord’s prayer, specifically Matthew 6:13. This is a great reference for SongRefs.</p>

<p><b>What if the lyrical phrase occurs in multiple Bible passages?</b></p>

<p>For the Coldplay example above, the Lord’s Prayer occurs in multiple Gospels. Sometimes you can narrow down a specific passage if the rest of the song contains references that only fit one passage, but generally it’s safe to be consistent and just choose the first occurrence. (This means the Gospels will have a disproportionate number of Matthew references.) This will allow us to add cross-reference support to eventually link the other passages to the first occurrence.</p>

<p><b>How long should the lyrical quote be?</b></p>

<p>Usually, at least several words. Sometimes, something with as few as three will be unique enough to clearly be quoting a specific verse, but usually you the lyric quote will need to be a little longer.</p>

<p>Example. When mewithoutYou sings,</p>

	<blockquote>“All creation groans”</blockquote>

<p>This is clearly a reference to Romans 8:22, but it works because creation and groan are both fairly uncommon words. This is a great reference for SongRefs.</p>

<p>On the other hand, when a worship song includes the words “Son of Man,” this phrase occurs numerous times in the Bible and it is not clear (unless the words are part of a longer reference) that the songwriter is thinking of a specific verse. This would not be a good reference for SongRefs.</p>

<p><b>What if the quote doesn’t use the whole verse?</b></p>

<p>To keep things simple, we’re just linking lyric quotes to verses and displaying the whole verse, so don’t worry if the lyric quote only uses part of the verse, as long as it’s clear that the quote really is referring to that specific verse.</p>

<p><b>Which Bible version should I use?</b></p>

<p>In general, try to find the version that matches as many words from the lyric as possible (use the biblehub link in the form or search biblegateway to compare). For example, Micah 7 has enough words and ideas in common with Jon Foreman’s “Equally Skilled” that almost any version is clearly good for multiple references. However, when you look at NASB, it is clear that Foreman is quoting nearly exactly from that version, so NASB is the best version to use.</p>

<p>If no version seems particularly close but the lyric is still a clear quote, just pick a common version. In the mewithoutYou example above, no version uses the exact words “all creation groans” - some use “whole” instead of “all” and some use other verb tenses such as groaning or groaneth, but again, the words are uncommon enough that it’s still a clear reference. So there are several common versions that are equally good to use for the reference.</p>

<p>Pro tip: Sometimes you can’t tell which version was used from a single verse, but if the song contains multiple references from that chapter, sometimes by comparing all of them a particular version emerges as the clear influence on the songwriter.</p>

<p><b>How do I know which artist or album to use?</b></p>

<p>Generally, for worship songs where there’s not a single iconic recording by a specific artist, we want to use the songwriter and we can leave the album blank. For most ‘regular’ songs by a specific band that was released on a specific album, we want the band name and the album name.</p>

